-- Snowflake SQL script to create raw food delivery tables and load sample data
-- Includes controlled data quality issues for later cleaning

CREATE DATABASE IF NOT EXISTS FOOD_DELIVERY_DB;
USE DATABASE FOOD_DELIVERY_DB;

CREATE SCHEMA IF NOT EXISTS RAW;
USE SCHEMA RAW;

CREATE OR REPLACE TABLE RESTAURANTS (
    RESTAURANT_ID NUMBER,
    RESTAURANT_NAME STRING,
    CITY STRING,
    CUISINE_TYPE STRING,
    RATING NUMBER(2,1)
);

CREATE OR REPLACE TABLE CUSTOMERS (
    CUSTOMER_ID NUMBER,
    CUSTOMER_NAME STRING,
    CITY STRING,
    SIGNUP_DATE DATE
);

CREATE OR REPLACE TABLE FOOD_ORDERS (
    ORDER_ID NUMBER,
    CUSTOMER_ID NUMBER,
    RESTAURANT_ID NUMBER,
    ORDER_DATE DATE,
    ORDER_AMOUNT NUMBER(10,2),
    DELIVERY_FEE NUMBER(10,2),
    DISCOUNT_AMOUNT NUMBER(10,2),
    ORDER_STATUS STRING,
    PAYMENT_METHOD STRING,
    DELIVERY_MINUTES NUMBER
);

