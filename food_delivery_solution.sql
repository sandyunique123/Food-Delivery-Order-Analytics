USE SCHEMA STAGING;

CREATE OR REPLACE TABLE ORDERS_DEDUPED AS
SELECT
    ORDER_ID,
    CUSTOMER_ID,
    RESTAURANT_ID,
    ORDER_DATE,
    ORDER_STATUS,
    ORDER_AMOUNT,
    DELIVERY_TIME_MINUTES
FROM (
    SELECT
        o.ORDER_ID,
        o.CUSTOMER_ID,
        o.RESTAURANT_ID,
        o.ORDER_DATE,
        o.ORDER_STATUS,
        o.ORDER_AMOUNT,
        o.DELIVERY_TIME_MINUTES,
        ROW_NUMBER() OVER (PARTITION BY o.ORDER_ID ORDER BY o.CREATED_AT) AS rn
    FROM RAW.FOOD_ORDERS o
    WHERE o.ORDER_ID IS NOT NULL
      AND o.CUSTOMER_ID IS NOT NULL
      AND o.RESTAURANT_ID IS NOT NULL
      AND o.ORDER_AMOUNT >= 0
      AND o.ORDER_STATUS IN ('DELIVERED', 'CANCELLED', 'IN_PROGRESS')
      AND o.CUSTOMER_ID IN (SELECT CUSTOMER_ID FROM RAW.CUSTOMERS)
      AND o.RESTAURANT_ID IN (SELECT RESTAURANT_ID FROM RAW.RESTAURANTS)
      AND (
          (o.ORDER_STATUS = 'CANCELLED' AND o.DELIVERY_TIME_MINUTES IS NULL)
          OR (o.ORDER_STATUS IN ('DELIVERED', 'IN_PROGRESS') AND o.DELIVERY_TIME_MINUTES IS NOT NULL AND o.DELIVERY_TIME_MINUTES > 0)
      )
) AS deduped
WHERE rn = 1;

USE SCHEMA ANALYTICS;

CREATE OR REPLACE VIEW INVALID_ORDERS AS
SELECT
    ORDER_ID,
    CUSTOMER_ID,
    RESTAURANT_ID,
    ORDER_DATE,
    ORDER_STATUS,
    ORDER_AMOUNT,
    DELIVERY_TIME_MINUTES,
    INVALID_REASON
FROM (
    SELECT
        o.ORDER_ID,
        o.CUSTOMER_ID,
        o.RESTAURANT_ID,
        o.ORDER_DATE,
        o.ORDER_STATUS,
        o.ORDER_AMOUNT,
        o.DELIVERY_TIME_MINUTES,
        CASE
            WHEN COUNT(*) OVER (PARTITION BY o.ORDER_ID) > 1 THEN 'DUPLICATE_ORDER_ID'
            WHEN o.CUSTOMER_ID IS NULL THEN 'MISSING_CUSTOMER_ID'
            WHEN NOT EXISTS (SELECT 1 FROM RAW.CUSTOMERS c WHERE c.CUSTOMER_ID = o.CUSTOMER_ID) THEN 'UNKNOWN_CUSTOMER'
            WHEN o.RESTAURANT_ID IS NULL THEN 'MISSING_RESTAURANT_ID'
            WHEN NOT EXISTS (SELECT 1 FROM RAW.RESTAURANTS r WHERE r.RESTAURANT_ID = o.RESTAURANT_ID) THEN 'UNKNOWN_RESTAURANT'
            WHEN o.ORDER_AMOUNT < 0 THEN 'NEGATIVE_ORDER_AMOUNT'
            WHEN o.ORDER_STATUS NOT IN ('DELIVERED', 'CANCELLED', 'IN_PROGRESS') THEN 'INVALID_STATUS'
            WHEN o.ORDER_STATUS IN ('DELIVERED', 'IN_PROGRESS') AND (o.DELIVERY_TIME_MINUTES IS NULL OR o.DELIVERY_TIME_MINUTES <= 0) THEN 'INVALID_DELIVERY_TIME'
            ELSE 'VALID'
        END AS INVALID_REASON
    FROM RAW.FOOD_ORDERS o
) AS ranked
WHERE INVALID_REASON <> 'VALID';

CREATE OR REPLACE VIEW CLEAN_ORDERS AS
SELECT
    o.ORDER_ID,
    o.CUSTOMER_ID,
    o.RESTAURANT_ID,
    o.ORDER_DATE,
    o.ORDER_STATUS,
    o.ORDER_AMOUNT,
    o.DELIVERY_TIME_MINUTES,
    c.FIRST_NAME || ' ' || c.LAST_NAME AS CUSTOMER_NAME,
    r.RESTAURANT_NAME,
    r.CITY
FROM STAGING.ORDERS_DEDUPED o
LEFT JOIN RAW.CUSTOMERS c ON o.CUSTOMER_ID = c.CUSTOMER_ID
LEFT JOIN RAW.RESTAURANTS r ON o.RESTAURANT_ID = r.RESTAURANT_ID;

CREATE OR REPLACE VIEW RESTAURANT_PERFORMANCE AS
SELECT
    r.RESTAURANT_ID,
    r.RESTAURANT_NAME,
    COUNT(*) AS TOTAL_ORDERS,
    SUM(CASE WHEN o.ORDER_STATUS = 'DELIVERED' THEN o.ORDER_AMOUNT ELSE 0 END) AS DELIVERED_REVENUE,
    SUM(CASE WHEN o.ORDER_STATUS = 'CANCELLED' THEN 1 ELSE 0 END) AS CANCELLATION_COUNT,
    AVG(CASE WHEN o.ORDER_STATUS = 'DELIVERED' THEN o.DELIVERY_TIME_MINUTES END) AS AVG_DELIVERY_TIME_MINUTES
FROM ANALYTICS.CLEAN_ORDERS o
JOIN RAW.RESTAURANTS r ON o.RESTAURANT_ID = r.RESTAURANT_ID
GROUP BY 1, 2;

CREATE OR REPLACE VIEW KPI_SUMMARY AS
SELECT
    COUNT(*) AS TOTAL_ORDER_COUNT,
    SUM(CASE WHEN ORDER_STATUS = 'DELIVERED' THEN ORDER_AMOUNT ELSE 0 END) AS TOTAL_DELIVERED_REVENUE,
    SUM(CASE WHEN ORDER_STATUS = 'CANCELLED' THEN 1 ELSE 0 END) AS TOTAL_CANCELLATIONS,
    AVG(CASE WHEN ORDER_STATUS = 'DELIVERED' THEN DELIVERY_TIME_MINUTES END) AS AVG_DELIVERY_TIME_MINUTES,
    ROUND(SUM(CASE WHEN ORDER_STATUS = 'DELIVERED' THEN ORDER_AMOUNT ELSE 0 END) / NULLIF(COUNT(*), 0), 2) AS AVG_ORDER_VALUE
FROM ANALYTICS.CLEAN_ORDERS;

-- Analytical query 1: delivered revenue by restaurant
SELECT
    RESTAURANT_NAME,
    DELIVERED_REVENUE
FROM ANALYTICS.RESTAURANT_PERFORMANCE
ORDER BY DELIVERED_REVENUE DESC;

-- Analytical query 2: cancellation rate by restaurant
SELECT
    RESTAURANT_NAME,
    CANCELLATION_COUNT,
    ROUND(CANCELLATION_COUNT * 100.0 / NULLIF(TOTAL_ORDERS, 0), 2) AS CANCELLATION_RATE_PERCENT
FROM ANALYTICS.RESTAURANT_PERFORMANCE
ORDER BY CANCELLATION_RATE_PERCENT DESC;

-- Analytical query 3: average delivery time by restaurant
SELECT
    RESTAURANT_NAME,
    ROUND(AVG_DELIVERY_TIME_MINUTES, 1) AS AVG_DELIVERY_TIME_MINUTES
FROM ANALYTICS.RESTAURANT_PERFORMANCE
ORDER BY AVG_DELIVERY_TIME_MINUTES;

-- Analytical query 4: top customers by total spend
SELECT
    CUSTOMER_NAME,
    ROUND(SUM(ORDER_AMOUNT), 2) AS TOTAL_SPEND
FROM ANALYTICS.CLEAN_ORDERS
GROUP BY CUSTOMER_NAME
ORDER BY TOTAL_SPEND DESC;

-- Analytical query 5: delivered revenue trend by order date
SELECT
    ORDER_DATE,
    ROUND(SUM(CASE WHEN ORDER_STATUS = 'DELIVERED' THEN ORDER_AMOUNT ELSE 0 END), 2) AS DELIVERED_REVENUE
FROM ANALYTICS.CLEAN_ORDERS
GROUP BY ORDER_DATE
ORDER BY ORDER_DATE;
