);

CREATE OR REPLACE TABLE CUSTOMERS (
    CUSTOMER_ID NUMBER(9,0) PRIMARY KEY,
    FIRST_NAME STRING NOT NULL,
    LAST_NAME STRING NOT NULL,
    EMAIL STRING,
    SIGNUP_DATE DATE
