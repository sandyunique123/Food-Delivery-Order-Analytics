    ORDER_ID NUMBER(9,0),
    CUSTOMER_ID NUMBER(9,0),
    RESTAURANT_ID NUMBER(9,0),
    ORDER_DATE DATE,
    ORDER_STATUS STRING,
    ORDER_AMOUNT NUMBER(10,2),
    DELIVERY_TIME_MINUTES NUMBER(9,0),
    CREATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP
);

