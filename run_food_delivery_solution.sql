USE SCHEMA STAGING;

CREATE OR REPLACE VIEW INVALID_ORDERS AS
SELECT
    o.ORDER_ID,
    CASE
        WHEN o.ORDER_AMOUNT <= 0 THEN 'ORDER_AMOUNT must be greater than 0'
        WHEN o.ORDER_STATUS NOT IN ('PLACED', 'PREPARING', 'OUT_FOR_DELIVERY', 'DELIVERED', 'CANCELLED') THEN 'ORDER_STATUS is invalid'
        WHEN NOT EXISTS (SELECT 1 FROM RAW.RESTAURANTS r WHERE r.RESTAURANT_ID = o.RESTAURANT_ID) THEN 'RESTAURANT_ID does not exist in RAW.RESTAURANTS'
        WHEN o.ORDER_STATUS = 'DELIVERED' AND o.DELIVERY_MINUTES IS NULL THEN 'DELIVERED orders must have DELIVERY_MINUTES'
        ELSE 'UNKNOWN_VALIDATION_ISSUE'
    END AS VALIDATION_ISSUE,
    o.ORDER_AMOUNT,
    o.ORDER_STATUS,
    o.RESTAURANT_ID
FROM RAW.FOOD_ORDERS o
WHERE o.ORDER_AMOUNT <= 0
   OR o.ORDER_STATUS NOT IN ('PLACED', 'PREPARING', 'OUT_FOR_DELIVERY', 'DELIVERED', 'CANCELLED')
   OR NOT EXISTS (SELECT 1 FROM RAW.RESTAURANTS r WHERE r.RESTAURANT_ID = o.RESTAURANT_ID)
   OR (o.ORDER_STATUS = 'DELIVERED' AND o.DELIVERY_MINUTES IS NULL);

CREATE OR REPLACE VIEW CLEAN_ORDERS AS
SELECT
    o.ORDER_ID,
    o.CUSTOMER_ID,
    o.RESTAURANT_ID,
    o.ORDER_DATE,
    o.ORDER_AMOUNT,
    o.DELIVERY_FEE,
    o.DISCOUNT_AMOUNT,
    o.ORDER_STATUS,
    o.PAYMENT_METHOD,
    o.DELIVERY_MINUTES,
    (o.ORDER_AMOUNT + o.DELIVERY_FEE - o.DISCOUNT_AMOUNT) AS NET_ORDER_VALUE,
    r.RESTAURANT_NAME,
    r.CITY AS RESTAURANT_CITY,
    r.CUISINE_TYPE,
    r.RATING
FROM (
    SELECT fo.*, ROW_NUMBER() OVER (PARTITION BY fo.ORDER_ID ORDER BY fo.ORDER_DATE) AS rn
    FROM RAW.FOOD_ORDERS fo
) o
JOIN RAW.RESTAURANTS r ON o.RESTAURANT_ID = r.RESTAURANT_ID
LEFT JOIN STAGING.INVALID_ORDERS i ON o.ORDER_ID = i.ORDER_ID
WHERE o.rn = 1
  AND i.ORDER_ID IS NULL;

USE SCHEMA ANALYTICS;

CREATE OR REPLACE VIEW RESTAURANT_PERFORMANCE AS
SELECT
    c.RESTAURANT_ID,
    c.RESTAURANT_NAME,
    SUM(CASE WHEN c.ORDER_STATUS = 'DELIVERED' THEN c.NET_ORDER_VALUE ELSE 0 END) AS TOTAL_DELIVERED_REVENUE,
    COUNT(CASE WHEN c.ORDER_STATUS = 'DELIVERED' THEN 1 END) AS DELIVERED_ORDER_COUNT,
    COUNT(CASE WHEN c.ORDER_STATUS = 'CANCELLED' THEN 1 END) AS CANCELLED_ORDER_COUNT,
    AVG(CASE WHEN c.ORDER_STATUS = 'DELIVERED' THEN c.DELIVERY_MINUTES END) AS AVERAGE_DELIVERY_MINUTES,
    AVG(c.RATING) AS AVERAGE_RESTAURANT_RATING
FROM STAGING.CLEAN_ORDERS c
GROUP BY c.RESTAURANT_ID, c.RESTAURANT_NAME;

CREATE OR REPLACE VIEW KPI_SUMMARY AS
SELECT
    COUNT(*) AS TOTAL_ORDERS,
    SUM(CASE WHEN ORDER_STATUS = 'DELIVERED' THEN NET_ORDER_VALUE ELSE 0 END) AS TOTAL_DELIVERED_REVENUE,
    COUNT(CASE WHEN ORDER_STATUS = 'CANCELLED' THEN 1 END) AS TOTAL_CANCELLED_ORDERS,
    COUNT(DISTINCT CUSTOMER_ID) AS TOTAL_CUSTOMERS_SERVED,
    AVG(ORDER_AMOUNT) AS AVERAGE_ORDER_VALUE,
    AVG(CASE WHEN ORDER_STATUS = 'DELIVERED' THEN DELIVERY_MINUTES END) AS AVERAGE_DELIVERY_TIME_MINUTES
FROM STAGING.CLEAN_ORDERS;
